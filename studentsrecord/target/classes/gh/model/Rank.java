package model;

public class Rank {
	private Long id;
	private Integer classRank;
	private Integer hostelRank;
	private Integer districtRank;
	private Integer stateRank;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getClassRank() {
		return classRank;
	}
	public void setClassRank(Integer classRank) {
		this.classRank = classRank;
	}
	public Integer getHostelRank() {
		return hostelRank;
	}
	public void setHostelRank(Integer hostelRank) {
		this.hostelRank = hostelRank;
	}
	public Integer getDistrictRank() {
		return districtRank;
	}
	public void setDistrictRank(Integer districtRank) {
		this.districtRank = districtRank;
	}
	public Integer getStateRank() {
		return stateRank;
	}
	public void setStateRank(Integer stateRank) {
		this.stateRank = stateRank;
	}
	
}
